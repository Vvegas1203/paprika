# Read words.csv and generate translations
with open('public/words.csv', 'r', encoding='utf-8') as f:
    reader = csv.DictReader(f)
    en_translations = {}
    ar_translations = {}
    
    for row in reader:
        word_id = row['id']
        ru_text = row['translation']
        
        # Get English translation or fallback to Russian
        en_translations[word_id] = common_en.get(ru_text, ru_text)
        
        # Get Arabic translation or fallback to Russian
        ar_translations[word_id] = common_ar.get(ru_text, ru_text)

# Save English translations
with open('public/translations_en.json', 'w', encoding='utf-8') as f:
    json.dump(en_translations, f, ensure_ascii=False, indent=2)

# Save Arabic translations
with open('public/translations_ar.json', 'w', encoding='utf-8') as f:
    json.dump(ar_translations, f, ensure_ascii=False, indent=2)

print(f'Generated {len(en_translations)} English translations')
print(f'Generated {len(ar_translations)} Arabic translations')